public class MWQuestion2w8 {
    /*Print multiplication table of 24, 50 and 29 using loop.*/

    public static void main(String [] args){

        int i = 0;
        for (i = 1;i <= 10; i++){
            System.out.println("24 x "+ i + " = "+(24*i));
        }
        System.out.println("\n");
        int y = 1;
        for(y = 1; y<= 10; y++){
            System.out.println("50 x "+ y +" = "+(50*y));

        }
        System.out.println("\n");
        int z = 1;
        for(z = 1; z <= 10; z++){
            System.out.println("29 x "+ z +" = "+(29*z));

        }
    }
}
